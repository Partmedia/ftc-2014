#include "../lib/drive.c"

void init() {
    drive_init(Left, Right);
}

task main() {
    waitForStart();
    init();

    while (true) {
        getJoystickSettings(joystick);
        drive_handle_joystick();
        motor[Rack] = joystick.joy2_y1 * 0.5;
        motor[Lever] = joystick.joy2_y2 * 0.25;

        if (joy2Btn(6)) {
            motor[Squid] = 30;
        } else if (joy2Btn(8)) {
            motor[Squid] = -20;
        } else {
            motor[Squid] = 0;
        }

        if (joy2Btn(5)) {
            motor[winder] = 100;
        } else if (joy2Btn(7)) {
            motor[winder] = -100;
        } else {
            motor[winder] = 0;
        }

        // Servo controls (moveable goal grabbers). //
        if (joy1Btn(5)) {
            servo[Grabber] = 180;
        }
        if (joy1Btn(7)) {
            servo[Grabber] = -180;
        }
    }
}
