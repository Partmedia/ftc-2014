#include "common.c"

// Currently, init_common() starts robot with grabbers open.
static bool servo_isopen = true;                //< Keep track of current grabber state

static void init() {
    _drive_power = 100;
}

task main() {
    waitForStart();
    init_common();
    init();

    bool debounce_6 = true; //< True if button 6 is ready to be pressed

    while (true) {
        drive_handle_joystick();
        drive_switch_handle();

        if (joy1Btn(6)) {
            if (debounce_6 == true) {
	            if (servo_isopen) {
	                grabber_down(false);
	            } else {
	                grabber_up(false);
	            }
	            servo_isopen = !servo_isopen;
                debounce_6 = false;
            }
        } else {
            debounce_6 = true;
        }
    }
}
